package com.syntax.class07;

public class LoopHW1 {

	public static void main(String[] args) {
		
		//  1. Print numbers from 1 to 100 in 1 line with space
		
		int b=1;
		
		while (b<=100) {
			System.out.print(b+" ");
			b++;
		}
	

		//2. Print numbers from 100 to 1

		
		int a=100;
		
		while (a>=1) {
			System.out.println(a+" ");
			
			a--;
		}
		


	}

}
